package com.zunaid.Day5.details;

/**
 *
 * @author Zunaid
 */
public class Nationality {
    private int id;
    private String name;

    public Nationality() {
    }

    public Nationality(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
    
}
