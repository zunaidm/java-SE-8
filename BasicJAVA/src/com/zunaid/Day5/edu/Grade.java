
package com.zunaid.Day5.edu;


public class Grade {
    private int id;
    private double cgpa;

    public Grade() {
    }

    public Grade(int id, double cgpa) {
        this.id = id;
        this.cgpa = cgpa;
    }

    public int getId() {
        return id;
    }

    public double getCgpa() {
        return cgpa;
    }
    
    
    
}
