package com.zunaid.Day5.edu;

/**
 *
 * @author Zunaid
 */
public class PassingYear {
    private int id;
    private int year;

    public PassingYear() {
    }

    public PassingYear(int id, int year) {
        this.id = id;
        this.year = year;
    }

    public int getId() {
        return id;
    }

    public int getYear() {
        return year;
    }
    
    
}
